# Keywords in Python Programming

# Keywords are the words that convey a special meaning to the language compiler/interpreter. These are reserved for special purpose and must not be used as normal identifier names. Eg: for,break,continue,etc.

# 1. A keyword is a word having special meaning reserved by programming language
# 2. We cannot use a keyword as a variable name, function name or any other identifier.
# 3. True and False are truth values in Python. They are the results of comparison operations or logical (Boolean) operations in Python.
# 4. None is a special constant in Python that represents the absence of a value or a null value.
# 5. and, or, not are the logical operators in Python. and will result into True only if both the operands are True.
# 6. The async and await keywords are provided by the asyncio library in Python. They are used to write concurrent code in Python.

